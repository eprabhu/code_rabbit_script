            WHERE APT2.AWARD_ID = T1.AWARD_ID AND APT2.NON_EMPLOYEE_FLAG = ''Y''
              AND APT2.PERSON_ID IN (', @PROJECT_TEAM_VAL, ')))
    )');

    SET @CRT_AWD_CONTACT_CRT = CONCAT('(
        ( ''', @CONTACT_IS_EMP, ''' = ''true'' AND EXISTS (
            SELECT 1 FROM AWARD_CONTACT AC2
            WHERE AC2.AWARD_ID = T1.AWARD_ID
              AND AC2.PERSON_ID IN (', @CONTACT_VAL, ')))
         OR
        ( ''', @CONTACT_IS_EMP, ''' = ''false'' AND EXISTS (
            SELECT 1 FROM AWARD_CONTACT AC2
            WHERE AC2.AWARD_ID = T1.AWARD_ID
              AND AC2.ROLODEX_ID IN (', @CONTACT_VAL, ')))
    )');

    SET @CRT_AWD_SPL_REVIEW_PROTOCOL_CRT = CONCAT(
        '(EXISTS (SELECT 1 FROM AWARD_SPECIAL_REVIEW ASR2',
        ' WHERE ASR2.AWARD_ID = T1.AWARD_ID',
        ' AND LOWER(ASR2.PROTOCOL_NUMBER) LIKE ''%', @AWD_SPL_REVIEW_PROTOCOL_VAL, '%''))'
    );

    IF @LAST_MOD_DATE_FROM IS NOT NULL AND @LAST_MOD_DATE_TO IS NOT NULL THEN
        SET @CRT_AWD_LAST_MOD_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.UPDATE_TIMESTAMP AS DATE) BETWEEN ''', @LAST_MOD_DATE_FROM, ''' AND ''', @LAST_MOD_DATE_TO, ''' )');
    ELSEIF @LAST_MOD_DATE_FROM IS NOT NULL AND @LAST_MOD_DATE_TO IS NULL THEN
        SET @CRT_AWD_LAST_MOD_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.UPDATE_TIMESTAMP AS DATE) >= ''', @LAST_MOD_DATE_FROM, ''' )');
    ELSEIF @LAST_MOD_DATE_FROM IS NULL AND @LAST_MOD_DATE_TO IS NOT NULL THEN
        SET @CRT_AWD_LAST_MOD_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.UPDATE_TIMESTAMP AS DATE) <= ''', @LAST_MOD_DATE_TO, ''' )');
    END IF;

    IF @EFFECTIVE_DATE_FROM IS NOT NULL AND @EFFECTIVE_DATE_TO IS NOT NULL THEN
        SET @CRT_AWD_EFFECTIVE_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.BEGIN_DATE AS DATE) BETWEEN ''', @EFFECTIVE_DATE_FROM, ''' AND ''', @EFFECTIVE_DATE_TO, ''' )');
    ELSEIF @EFFECTIVE_DATE_FROM IS NOT NULL AND @EFFECTIVE_DATE_TO IS NULL THEN
        SET @CRT_AWD_EFFECTIVE_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.BEGIN_DATE AS DATE) >= ''', @EFFECTIVE_DATE_FROM, ''' )');
    ELSEIF @EFFECTIVE_DATE_FROM IS NULL AND @EFFECTIVE_DATE_TO IS NOT NULL THEN
        SET @CRT_AWD_EFFECTIVE_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.BEGIN_DATE AS DATE) <= ''', @EFFECTIVE_DATE_TO, ''' )');
    END IF;

    IF @FINAL_EXP_DATE_FROM IS NOT NULL AND @FINAL_EXP_DATE_TO IS NOT NULL THEN
        SET @CRT_AWD_FINAL_EXPIRATION_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.FINAL_EXPIRATION_DATE AS DATE) BETWEEN ''', @FINAL_EXP_DATE_FROM, ''' AND ''', @FINAL_EXP_DATE_TO, ''' )');
    ELSEIF @FINAL_EXP_DATE_FROM IS NOT NULL AND @FINAL_EXP_DATE_TO IS NULL THEN
        SET @CRT_AWD_FINAL_EXPIRATION_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.FINAL_EXPIRATION_DATE AS DATE) >= ''', @FINAL_EXP_DATE_FROM, ''' )');
    ELSEIF @FINAL_EXP_DATE_FROM IS NULL AND @FINAL_EXP_DATE_TO IS NOT NULL THEN
        SET @CRT_AWD_FINAL_EXPIRATION_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.FINAL_EXPIRATION_DATE AS DATE) <= ''', @FINAL_EXP_DATE_TO, ''' )');
    END IF;

    IF @NOTICE_DATE_FROM IS NOT NULL AND @NOTICE_DATE_TO IS NOT NULL THEN
        SET @CRT_AWD_NOTICE_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.AWARD_EFFECTIVE_DATE AS DATE) BETWEEN ''', @NOTICE_DATE_FROM, ''' AND ''', @NOTICE_DATE_TO, ''' )');
    ELSEIF @NOTICE_DATE_FROM IS NOT NULL AND @NOTICE_DATE_TO IS NULL THEN
        SET @CRT_AWD_NOTICE_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.AWARD_EFFECTIVE_DATE AS DATE) >= ''', @NOTICE_DATE_FROM, ''' )');
    ELSEIF @NOTICE_DATE_FROM IS NULL AND @NOTICE_DATE_TO IS NOT NULL THEN
        SET @CRT_AWD_NOTICE_DATE_CRT = CONCAT('( T1.AWARD_ID IS NOT NULL AND CAST(T1.AWARD_EFFECTIVE_DATE AS DATE) <= ''', @NOTICE_DATE_TO, ''' )');
    END IF;

    -- Build the complete criteria map: maps each filter key to its SQL WHERE fragment
    SET @CRITERIA_MAP = JSON_OBJECT(
        'CRT_AWD_ID',                       CONCAT('(T1.AWARD_ID LIKE ''%', @AWD_ID_VAL, '%'')'),
        'CRT_AWD_NUMBER',                    CONCAT('(T1.AWARD_NUMBER LIKE ''%', @AWD_NUMBER_VAL, '%'')'),
        'CRT_AWD_ACCOUNT_NUMBER',            CONCAT('(T1.ACCOUNT_NUMBER LIKE ''%', @AWD_ACCOUNT_NUMBER_VAL, '%'')'),
        'CRT_AWD_TITLE',                     CONCAT('(T1.TITLE LIKE ''%', @AWD_TITLE_VAL, '%'')'),
        'CRT_AWD_SPONSOR_AWARD_NUMBER',      CONCAT('(T1.SPONSOR_AWARD_NUMBER LIKE ''%', @AWD_SPONSOR_AWARD_NUMBER_VAL, '%'')'),
        'CRT_AWD_CFDA_NUMBER',               CONCAT('(T1.CFDA_NUMBER LIKE ''%', @AWD_CFDA_NUMBER_VAL, '%'')'),
        'CRT_AWD_DFAFS_NUMBER',              CONCAT('(T1.DFAFS_NUMBER LIKE ''%', @AWD_DFAFS_NUMBER_VAL, '%'')'),
        'CRT_AWD_TYPE',                      CONCAT('(', IFNULL(CONCAT('T1.AWARD_TYPE_CODE IN (', @AWD_TYPE_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_LEAD_UNIT',                 CONCAT('(', IFNULL(CONCAT('T1.LEAD_UNIT_NUMBER IN (', @AWD_LEAD_UNIT_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_PRIME_SPONSOR',             CONCAT('(', IFNULL(CONCAT('T1.PRIME_SPONSOR_CODE IN (', @AWD_PRIME_SPONSOR_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_SPONSOR',                   CONCAT('(', IFNULL(CONCAT('T1.SPONSOR_CODE IN (', @AWD_SPONSOR_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_STATUS',                    CONCAT('(', IFNULL(CONCAT('T1.STATUS_CODE IN (', @AWD_STATUS_VAL, ')'), '1=1'), ')'),
		'CRT_AWD_GRANT_CALL_ABBREVIATION', CONCAT('(T7.ABBREVIATION LIKE ''%', @AWD_GRANT_CALL_VAL, '%'')'),
        'CRT_AWD_ACCOUNT_TYPE',              CONCAT('(', IFNULL(CONCAT('T1.ACCOUNT_TYPE_CODE IN (', @AWD_ACCOUNT_TYPE_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_GRANT_TYPE',                CONCAT('(', IFNULL(CONCAT('GCT.GRANT_TYPE_CODE IN (', @AWD_GRANT_TYPE_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_ACTIVITY_TYPE',             CONCAT('(', IFNULL(CONCAT('T1.ACTIVITY_TYPE_CODE IN (', @AWD_ACTIVITY_TYPE_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_BUDGET_STATUS',             CONCAT('(', IFNULL(CONCAT('ABS.AWARD_BUDGET_STATUS_CODE IN (', @AWD_BUDGET_STATUS_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_VERSION_STATUS',            CONCAT('(', IFNULL(CONCAT('T1.AWARD_SEQUENCE_STATUS IN (', @AWD_VERSION_STATUS_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_RESEARCH_AREAS',            CONCAT('(', IFNULL(CONCAT('EXISTS (SELECT 1 FROM AWARD_RESEARCH_AREAS ARA2 WHERE ARA2.AWARD_ID = T1.AWARD_ID AND ARA2.RESRCH_TYPE_AREA_CODE IN (', @AWD_RESEARCH_AREAS_VAL, '))'), '1=1'), ')'),
        'CRT_AWD_PI',                        @CRT_AWD_PI_CRT,
        'CRT_AWD_KEYWORDS',                  CONCAT('(EXISTS(SELECT 1 FROM AWARD_SCIENCE_KEYWORD AK LEFT JOIN SCIENCE_KEYWORD SK ON AK.SCIENCE_KEYWORD_CODE = SK.SCIENCE_KEYWORD_CODE WHERE AK.AWARD_ID = T1.AWARD_ID AND (SK.DESCRIPTION LIKE ''%', @AWD_KEYWORDS_VAL, '%'' OR AK.KEYWORD LIKE ''%', @AWD_KEYWORDS_VAL, '%'')))'),
        'CRT_AWD_LAST_MOD_DATE',             @CRT_AWD_LAST_MOD_DATE_CRT,
        'CRT_AWD_EFFECTIVE_DATE',            @CRT_AWD_EFFECTIVE_DATE_CRT,
        'CRT_AWD_FINAL_EXPIRATION_DATE',     @CRT_AWD_FINAL_EXPIRATION_DATE_CRT,
        'CRT_AWD_NOTICE_DATE',               @CRT_AWD_NOTICE_DATE_CRT,
		'CRT_AWD_KEY_PERSON',   IFNULL(@CRT_AWD_KEY_PERSON_CRT, '1=1'),
		'CRT_AWD_KEY_PERSON_ROLES',  IFNULL(@AWARD_KEY_PERSONS_ROLES_CRT, '1=1'),
        'CRT_AWD_VERSION_TYPE', CONCAT('(', IFNULL(CONCAT('T1.AWARD_VARIATION_TYPE_CODE IN (', @AWD_VARIATION_TYPE_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_PROJECT_TEAM',              @CRT_AWD_PROJECT_TEAM_CRT,
        'CRT_AWD_CONTACT',                   @CRT_AWD_CONTACT_CRT,
        'CRT_AWD_SPL_REVIEW_PROTOCOL',       @CRT_AWD_SPL_REVIEW_PROTOCOL_CRT,
		'CRT_AWD_FUNDING_SCHEME',        CONCAT('(', IFNULL(CONCAT('FS.FUNDING_SCHEME_CODE IN (', @AWD_FUNDING_SCHEME_VAL, ')'), '1=1'), ')'),
        'CRT_AWD_GRANT_CALL',                CONCAT('(', IFNULL(CONCAT('T1.GRANT_HEADER_ID IN (', @AWD_GRANT_CALL_ID_VAL, ')'), '1=1'), ')')
    );

    -- Merge custom data criteria and build the final filter condition
    SELECT JSON_MERGE_PATCH(@CRITERIA_MAP, @CUST_CRITERIA_MAP) INTO @CRITERIA_MAP;
    SELECT FN_PREPARE_DASHBOARD_CRITERIA(AV_FILTER_JSON, @CRITERIA_MAP) INTO LS_FILTER_CONDITION;

    -- =============================================
    -- SORT TYPE
    -- Set default sort order if not provided
    -- =============================================
    IF AV_SORT_TYPE IS NULL OR AV_SORT_TYPE = '' THEN
        IF AV_TAB_TYPE = 'ALL_AWARDS' THEN
            SET AV_SORT_TYPE = 'T1.AWARD_NUMBER ASC';
        ELSE
            SET AV_SORT_TYPE = 'T1.UPDATE_TIMESTAMP DESC';
        END IF;
    ELSE
        SET AV_SORT_TYPE = CONCAT(AV_SORT_TYPE, ',T1.UPDATE_TIMESTAMP DESC');
    END IF;

    -- =============================================
    -- BUILD AND EXECUTE FINAL DYNAMIC SQL
    -- =============================================
    SET LS_DYN_SQL = CONCAT(@DYNAMIC_CTE,
        CASE WHEN AV_IS_COUNT = TRUE
            THEN ' SELECT TOTAL_COUNT FROM (SELECT COUNT(DISTINCT T1.AWARD_ID) AS TOTAL_COUNT'
            ELSE CONCAT(' SELECT ', IFNULL(@DISPLAY, '*'), ' FROM (SELECT ', IFNULL(@SELECT_STM, '*'))
        END,
        ' FROM AWARD T1 ',
        'LEFT JOIN AWARD_PERSONS AP ON T1.AWARD_ID = AP.AWARD_ID AND AP.PERSON_ROLE_ID = 3 ',
        'LEFT JOIN GRANT_CALL_HEADER T7 ON T7.GRANT_HEADER_ID = T1.GRANT_HEADER_ID ',
        'LEFT JOIN AWARD_STATUS T4 ON T4.STATUS_CODE = T1.STATUS_CODE ',
        @PENDING_JOINS,
        IFNULL(@JOIN_QUERY, ''),
        CASE WHEN @TAB_QUERY IS NOT NULL AND @TAB_QUERY <> '' THEN CONCAT(' ', @TAB_QUERY) ELSE '' END,
        CASE WHEN LS_FILTER_CONDITION IS NOT NULL AND LS_FILTER_CONDITION <> '' THEN CONCAT(' AND ', LS_FILTER_CONDITION) ELSE '' END,
        IF(AV_IS_COUNT = FALSE, ' GROUP BY T1.AWARD_ID ', ''),
        IF(AV_IS_COUNT = FALSE, CONCAT(' ORDER BY ', AV_SORT_TYPE, ' '), ''),
        IF(AV_IS_COUNT = FALSE, CONCAT(' LIMIT ', AV_LIMIT, ' OFFSET ', AV_OFFSET), ''),
        ') T'
    );

    -- Prepare and execute the dynamic SQL statement
    SET @QUERY_STATEMENT = LS_DYN_SQL;
    PREPARE EXECUTABLE_STATEMENT FROM @QUERY_STATEMENT;
    EXECUTE EXECUTABLE_STATEMENT;
    DEALLOCATE PREPARE EXECUTABLE_STATEMENT;
    SET SESSION optimizer_search_depth = old_depth;

END
