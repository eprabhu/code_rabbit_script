CREATE PROCEDURE GRANT_CALL_DUE_DATE_REMINDER(
    IN AV_DAYS INT
)
BEGIN
    SELECT
		3 AS MODULE_CODE,
		0 AS SUB_MODULE_CODE,
        0 AS SUB_MODULE_ITEM_KEY,
        DATE_FORMAT(GCH.INTERNAL_SUBMISSION_DEADLINE_DATE, '%d-%m-%Y') AS INTERNAL_DEADLINE,
        EP.PROPOSAL_ID AS MODULE_ITEM_KEY
		FROM GRANT_CALL_HEADER GCH
		INNER JOIN EPS_PROPOSAL EP ON EP.GRANT_HEADER_ID = GCH.GRANT_HEADER_ID AND EP.STATUS_CODE = 1
		INNER JOIN EPS_PROPOSAL_PERSONS EPP ON EPP.PROPOSAL_ID = EP.PROPOSAL_ID 
		WHERE GCH.INTERNAL_SUBMISSION_DEADLINE_DATE = DATE(DATE_ADD(UTC_TIMESTAMP(), INTERVAL AV_DAYS DAY))
		AND EPP.PROP_PERSON_ROLE_ID = 3;
END
