3,
EPS_PROPOSAL.PROPOSAL_NUMBER,
EPS_PROPOSAL.DOCUMENT_NUMBER,
EPS_PROPOSAL.TITLE,
UNIT.UNIT_NAME AS LEAD_UNIT,
EPS_PROPOSAL.OWNED_BY_UNIT AS LEAD_UNIT_NUMBER,
