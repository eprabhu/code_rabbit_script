MODULE_CODE, MODULE_ITEM_KEY, DOCUMENT_NUMBER, TITLE, LEAD_UNIT_NAME, LEAD_UNIT_NUMBER, STATUS, STATUS_CODE, SPONSOR_CODE, SPONSOR_NAME,
PERSON_ID, PI_NAME, LOAD_TIMESTAMP, START_DATE, END_DATE, CONTRACT_ADMIN_PERSON_ID, PRIME_SPONSOR_CODE)
