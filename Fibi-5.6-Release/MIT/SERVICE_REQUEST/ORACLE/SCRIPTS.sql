T1.PERSON_ID,
T1.FULL_NAME,
SYSDATE,
EPS_PROPOSAL.REQUESTED_START_DATE_INITIAL,
EPS_PROPOSAL.REQUESTED_END_DATE_INITIAL,
T2.PERSON_ID AS CONTRACT_ADMIN_PERSON_ID,
EPS_PROPOSAL.PRIME_SPONSOR_CODE
FROM EPS_PROPOSAL
INNER JOIN UNIT ON UNIT.UNIT_NUMBER = EPS_PROPOSAL.OWNED_BY_UNIT
LEFT OUTER JOIN SPONSOR ON SPONSOR.SPONSOR_CODE = EPS_PROPOSAL.SPONSOR_CODE
INNER JOIN PROPOSAL_STATE ON PROPOSAL_STATE.STATE_TYPE_CODE = EPS_PROPOSAL.STATUS_CODE
LEFT OUTER JOIN EPS_PROP_PERSON T1 ON EPS_PROPOSAL.PROPOSAL_NUMBER = T1.PROPOSAL_NUMBER and t1.PROP_PERSON_ROLE_ID = 'PI'
LEFT OUTER JOIN UNIT_ADMINISTRATOR T2 ON T2.UNIT_NUMBER = EPS_PROPOSAL.OWNED_BY_UNIT and T2.UNIT_ADMINISTRATOR_TYPE_CODE = '2'

-- Insert Script for MITKC_ELASTIC_INDEX(Award)
INSERT INTO MITKC_ELASTIC_INDEX@FIBISO_LINK(
MODULE_CODE, MODULE_ITEM_KEY, DOCUMENT_NUMBER, PERSON_ID, PI_NAME, ACCOUNT_NUMBER, LEAD_UNIT_NUMBER, LEAD_UNIT_NAME, TITLE, 
SPONSOR_CODE, SPONSOR_NAME, SPONSOR_AWARD_ID, LOAD_TIMESTAMP, STATUS, status_code, MODULE_ITEM_ID, START_DATE, END_DATE, OBLIGATED_TOTAL, 
ANTICIPATED_TOTAL_AMOUNT, OBLIGATION_START_DATE, OBLIGATION_EXPIRATION_DATE, CONTRACT_ADMIN_PERSON_ID, PRIME_SPONSOR_CODE)
SELECT
1 as MODULE_CODE,
t1.AWARD_NUMBER as MODULE_ITEM_KEY,
t1.DOCUMENT_NUMBER,
t3.PERSON_ID,
t3.FULL_NAME AS PI_NAME,
nvl(t1.ACCOUNT_NUMBER,' ') ACCOUNT_NUMBER,
t1.LEAD_UNIT_NUMBER,
T2.UNIT_NAME as LEAD_UNIT_NAME,
t1.TITLE,
t4.SPONSOR_CODE,
t4.SPONSOR_NAME,
t1.SPONSOR_AWARD_NUMBER,
SYSDATE,
T8.DESCRIPTION as STATUS,
T1.status_code,
t1.award_id,
t1.AWARD_EFFECTIVE_DATE,
t5.final_expiration_date,
t5.AMOUNT_OBLIGATED_TO_DATE,
t5.ANTICIPATED_TOTAL_AMOUNT,
t5.CURRENT_FUND_EFFECTIVE_DATE,
t5.OBLIGATION_EXPIRATION_DATE,
T7.PERSON_ID AS CONTRACT_ADMIN_PERSON_ID,
T1.PRIME_SPONSOR_CODE
FROM AWARD t1
INNER JOIN UNIT t2 on T1.LEAD_UNIT_NUMBER = T2.UNIT_NUMBER
LEFT OUTER JOIN AWARD_PERSONS t3 ON t1.AWARD_ID = t3.AWARD_ID AND CONTACT_ROLE_CODE = 'PI'
LEFT OUTER JOIN SPONSOR t4 ON t1.SPONSOR_CODE = t4.SPONSOR_CODE
LEFT OUTER JOIN UNIT_ADMINISTRATOR T7 ON T7.UNIT_NUMBER = T1.LEAD_UNIT_NUMBER and T7.UNIT_ADMINISTRATOR_TYPE_CODE = '2'
inner join award_amount_info t5 on t1.award_id = t5.award_id
LEFT OUTER JOIN AWARD_STATUS T8 on t1.STATUS_CODE = T8.STATUS_CODE
WHERE T1.AWARD_SEQUENCE_STATUS = 'ACTIVE'
AND T5.AWARD_AMOUNT_INFO_ID IN (SELECT MAX(AWARD_AMOUNT_INFO_ID) FROM award_amount_info t6 WHERE t6.award_id = t5.award_id)

